let passou = "lista";

switch (passou) {
    case "aprovada":
        console.log("Parabéns você passou!");        
        break;
    case "lista":
        console.log("Você está na lista de espera!");
        break;
    case "reprovada":
        console.log("Você foi reprovado!");
        break;
    default:
        console.log("Não se aplica!");
}